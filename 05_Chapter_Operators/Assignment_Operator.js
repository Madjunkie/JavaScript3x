// ======================
// ASSIGNMENT OPERATORS
// ======================

// SIMPLE ASSIGNMENT
let a = 10;
console.log("=  :", a); // 10

// ARITHMETIC ASSIGNMENT
a += 5;   // a = a + 5
console.log("+= :", a); // 15

a -= 3;   // a = a - 3
console.log("-= :", a); // 12

a *= 2;   // a = a * 2
console.log("*= :", a); // 24

a /= 4;   // a = a / 4
console.log("/= :", a); // 6

a %= 4;   // a = a % 4
console.log("%= :", a); // 2

a **= 3;  // a = a ** 3
console.log("**=:", a); // 8

